	<div class="card-body">
		<p class="card-text">Load Order Library is a website/tool to share mod lists of Bethesda games with other players. Primarily for debugging purposes, but can be quite useful for YouTuber's to have mod lists for each of their let's play characters, mod list creators to share with users, etc.</p>

		<p class="card-text">To upload a mod list, simply click "upload" and select the requested Mod Organizer files. Once uploaded, a handy link will be provided to share with others! <strong>An account is not required to upload, but recommended for extra features such as deleting lists.</strong> More features will come in the future. Checkout the <a href="https://github.com/phinocio/loadorderlibrary/blob/master/README.md">Github README</a> for more info.</p>
